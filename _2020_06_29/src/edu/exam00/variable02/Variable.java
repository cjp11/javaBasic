package edu.exam00.variable02;

/*
 * 변수란?
 * 메모리의 특정 공간에 이름을 붙여서
 * 접근 할 수 있도록 한 것
 * 숫자를 대입하면 해당 숫자를 의미하게 된다.
 * = 를 기준으로 왼쪽은 공간, 오른쪽은 값의 의미를 가진다.
 * 
 * 자동화에 있어서 반드시 필요
 * 
 */
public class Variable {
	public static void main(String[] args) {
		int num = 10;
		System.out.println(num);
		num = 100;
		System.out.println(num);
	}
}
